    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
     try{
         String sql="SELECT * FROM `marks` WHERE Id=?";
          pst=conn.prepareStatement(sql);
          pst.setString(1,tid.getText());
          rs= pst.executeQuery();
          if(rs.next()){
              JOptionPane.showMessageDialog(null, "Result Showed.");
              String add1=rs.getString("id");
              tid.setText(add1);
              String add2=rs.getString("bangla");
              tbangla.setText(add2);
              String add3=rs.getString("english");
              tenglish.setText(add3);
              String add4=rs.getString("math");
              tmath.setText(add4);
              String add5=rs.getString("ict");
              tict.setText(add5);
          }
          else {
                JOptionPane.showMessageDialog(null, "Result Not Found.", "Access Denied", JOptionPane.ERROR_MESSAGE);
                close();
                Result r=new Result();
                r.setVisible(true); 
                close();
          }
         
     }   
      catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

